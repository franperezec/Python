# Introducción a las clases en Python

mensaje = "Hola Mundo"
print(type(mensaje))  # <class 'str'> es un objeto de la clase str

# Clase: es el plano de construcción de un objeto
# Objeto: es una instancia de una clase

# Clases: son un conjunto de atributos y métodos que definen un objeto
# Atributos: son las características del objeto
# Métodos: son las acciones que puede realizar el objeto

# ejemplos de clases en Python
# str, int, float, list, dict, tuple, set, bool

# Clase: Humano (definición de la clase)
# Objeto: Juan, Ana, Pedro, María, Luis, etc. instancias de la clase Humano
# Atributos: nombre, edad, altura, peso (características de un humano)
# Métodos: caminar, correr, saltar, comer, dormir (acciones que puede realizar un humano)

# Clase: Coche (definición de la clase)
# Objeto: BMW, Audi, Mercedes, Toyota, etc. instancias de la clase Coche
# Atributos: marca, modelo, color, precio (características de un coche)
# Métodos: arrancar, acelerar, frenar, girar, apagar (acciones que puede realizar un coche)

# Clase: Animal (definición de la clase)
