# Comparadores en Python
print(1 > 2)  # False mayor que
print(1 < 2)  # True menor que
print(1 >= 1)  # True mayor o igual que
print(1 <= 4)  # True menor o igual que
print(1 == 1)  # True igual que
print(1 != 1)  # False diferente que
print(1 == "1")  # False igual que
print(1 == int("1"))  # True igual que
print(1 == float("1"))  # True igual que
print(1 == 1.0)  # True igual que
print(1 == 1.1)  # False igual que
