entero = 2  # int (entero)
decimal = 2.5  # float (decimal)
complejo = 2 + 5j  # complex (complejo)

# Propiedades
print(type(entero))
print(type(decimal))
print(type(complejo))

# Conversión de tipos
print(float(entero))
print(int(decimal))
print(complejo.real)
print(complejo.imag)

# Operaciones
print(2 + 3)  # Suma
print(2 - 3)  # Resta
print(2 * 3)  # Multiplicación
print(2 / 3)  # División
print(2 // 3)  # División entera redondeando hacia abajo
print(2 % 3)  # Módulo es la parte entera de la división
print(2 ** 3)  # Potencia

# entero = entero + 1
entero += 1  # Incremento en 1
print(entero)
entero -= 1  # Decremento en 1
print(entero)
entero *= 1  # Multiplicación por 1
print(entero)
entero /= 1  # División por 1
print(entero)
entero //= 1  # División entera por 1
print(entero)
entero %= 1  # Módulo por 1
print(entero)
entero **= 1  # Potencia por 1
print(entero)
