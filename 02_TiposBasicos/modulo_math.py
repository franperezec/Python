import math

print(round(2.5))  # Redondeo al entero más cercano (2)
print(abs(-2.5))  # Valor absoluto (2.5)
print(pow(2, 3))  # Potencia (8)

print(math.ceil(2.5))  # Redondeo hacia arriba (3)
print(math.floor(2.5))  # Redondeo hacia abajo (2)

print(math.isnan(float('nan')))  # Is not a number (isnan)? (True)
print(math.isnan(23.2))  # ¿Es un número no válido? (False)


print(math.pow(2, 3))  # Potencia (8.0)
print(math.sqrt(9))  # Raíz cuadrada (3.0)
print(math.exp(1))  # Exponencial (2.718281828459045)
print(math.log(10))  # Logaritmo natural (2.302585092994046)
print(math.log10(10))  # Logaritmo base 10 (1.0)
print(math.log2(8))  # Logaritmo base 2 (3.0)

print(math.isinf(float('inf')))  # ¿Es infinito? (True)
